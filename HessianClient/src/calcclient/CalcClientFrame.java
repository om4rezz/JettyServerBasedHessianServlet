    private void initEventDriven() {
        jbtGo.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                String operation = jcboOperation.getSelectedItem().toString();

                int n1 = Integer.parseInt(jtfNumber1.getText());
                int n2 = Integer.parseInt(jtfNumber2.getText());

                int result = 0;
                switch (operation) {
                    case "sum":
                        result = CalcClient.calcService.sum(n1, n2);
                        break;
                    case "subtract":
                        result = CalcClient.calcService.subtract(n1, n2);
                        break;
                    case "multiply":
                        result = CalcClient.calcService.multiply(n1, n2);
                        break;
                }
                JOptionPane.showMessageDialog(null, "Result: " + result);
            }
        });
    }
